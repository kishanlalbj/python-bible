# Boolean Operators

bit_1 = True
bit_2 = False

print("AND", bit_1 and bit_2)
print("OR", bit_1 or bit_2)
print("NOT 1", not bit_1)
print("NOT 2", not bit_2)



#Logical or Comparison Operators

number_1 = 200
number_2 = 300
number_3 = 500
print(number_1 > number_2)
print(number_2 == number_1)
print(number_1 < number_2)
print(number_1 != number_2)
print(number_2 >= number_1 <= number_3 )


# Conditional Operators

if  not number_1 > number_3 and number_1 < number_3:
    print("It is lower")
else:
    print("It is higher")

